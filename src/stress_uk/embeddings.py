@lru_cache(maxsize=1)
def _get_model():
    """Лінива (опційна) залежність - sentence-transformers важка й не всі
    хочуть її тягнути лише за стем-тригери. Якщо немає - модуль просто
    нічого не дає (None), без помилки."""
    try:
        from sentence_transformers import SentenceTransformer
    except ImportError:
        return None
    return SentenceTransformer("sentence-transformers/paraphrase-multilingual-MiniLM-L12-v2")


@lru_cache(maxsize=512)
def _centroid(word_lower: str, stressed: str) -> tuple[float, ...] | None:
    model = _get_model()
    if model is None:
        return None
    examples = SENSE_EXAMPLES.get(word_lower, {}).get(stressed)
    if not examples:
        return None
    embs = model.encode(examples)
    centroid = embs.mean(axis=0)
    return tuple(centroid.tolist())


def embedding_disambiguate(word_lower: str, segment_text: str) -> str | None:
    """segment_text: текст пунктуаційного сегмента (речення чи його
    частина), що містить word_lower - той самий контекст, що бачить
    стем-тригерний резолвер, але як суцільний рядок для ембедингу, не
    мішок слів. None, якщо немає прикладів для слова, немає
    sentence-transformers, чи менше 2 сенсів."""
    senses = SENSE_EXAMPLES.get(word_lower)
    if not senses or len(senses) < 2:
        return None
    model = _get_model()
    if model is None:
        return None

    import numpy as np

    centroids = {}
    for stressed in senses:
        c = _centroid(word_lower, stressed)
        if c is not None:
            centroids[stressed] = np.array(c)
    if len(centroids) < 2:
        return None

    target_emb = model.encode([segment_text])[0]
    scores = {
        stressed: float(np.dot(target_emb, c) / (np.linalg.norm(target_emb) * np.linalg.norm(c)))
        for stressed, c in centroids.items()
    }
    return max(scores, key=scores.get)
