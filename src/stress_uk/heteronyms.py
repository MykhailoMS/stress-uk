# Слова, для яких кеш (найчастіший варіант серед екстрагованих словоформ)
# СИСТЕМАТИЧНО дає неправильну відповідь - не через контекстну омонімію
# (як HETERONYMS вище), а через ваду самого підрахунку голосів у джерелі.
# Раніше тут патчились на виході "вони" (рідка лема-колізія "во́на"
# накручувала голоси за "во́ни" в dict/ Рабульця) і "опікуватися"-форми
# (одинока помилка джерела, нема з чим голосувати) - тепер ОБИДВА класи
# виправлені в самому build.py: (1) дедуплікація голосів за лемою +
# пріоритет джерел (СУМ-20 > Вікісловник > dict/) виправила "вони" без
# жодного патчу; (2) data/processed/manual_corrections.tsv (найвищий
# пріоритет, окрема "manual-"-лема) - вручну перевірені виправлення для
# випадків, де одноголосно помиляються ВСІ автоматичні джерела
# одразу - виправив "опікуватися". Список лишається порожнім як
# запобіжник для майбутніх одноразових багів того самого класу, які
# знайдуться РАНІШЕ, ніж буде час перегенерувати повний датасет.
ALWAYS_CORRECT: dict[str, str] = {}


def disambiguate(word_lower: str, context_words_lower: dict[str, int]) -> str | None:
    """word_lower: слово-кандидат у нижньому регістрі без наголосу.
    context_words_lower: сусідні слова речення (lower, без наголосу) ->
    відстань у словах до цільового слова (1 = сусіднє). Скор сенсу - сума
    1/відстань по всіх контекстних словах, що матчать хоч один його
    тригер-стем (ближче слово - сильніший доказ; той самий принцип, що
    "близькість - сильний сигнал", який раніше просто ігнорувався через
    `set` без відстаней). Переможець - сенс зі строго вищим скором, не
    "рівно один сенс матчнув": якщо триггер сильнішого (ближчого) сенсу
    переважує слабший (дальній) збіг іншого сенсу, тепер резолвиться,
    а не автоматично йде в None лише через факт, що щось матчнуло і там.
    -> акцентована форма, якщо є однозначний переможець, інакше None.

    Відома межа: якщо той самий омограф зустрічається ДВІЧІ в одному
    реченні з різними значеннями ("Скільки б роботи не намагалися забрати
    роботи у людей"), тригери обох сенсів потрапляють у те саме контекстне
    вікно одночасно - резолвер не може прив'язати конкретне входження до
    значення (рівні скори -> None, якщо відстані симетричні). Свідомо
    прийнятний, задокументований ризик (README) - rare contrived-case, де
    навіть людина-читач не завжди одразу вловлює правильний наголос без
    паузи на роздуми."""
    if word_lower in ALWAYS_CORRECT:
        return ALWAYS_CORRECT[word_lower]
    senses = HETERONYMS.get(word_lower)
    if not senses:
        return None

    scored = [
        (sum(1 / context_words_lower[w]
             for stem in s.context_stems
             for w in context_words_lower
             if w.startswith(stem)),
         s)
        for s in senses
    ]
    positive = sorted((sc for sc in scored if sc[0] > 0), key=lambda sc: sc[0], reverse=True)
    if not positive:
        return None
    if len(positive) == 1 or positive[0][0] > positive[1][0]:
        return positive[0][1].stressed
    return None
